#define SECTOR_SIZE   512
#define IDE_BSY       0x80
#define IDE_DRDY      0x40
#define IDE_DF        0x20
#define IDE_ERR       0x01

#define IDE_CMD_READ  0x20
#define IDE_CMD_WRITE 0x30
#define IDE_CMD_RDMUL 0xc4
#define IDE_CMD_WRMUL 0xc5

// idequeue points to the buf now being read/written to the disk.
// idequeue->qnext points to the next buf to be processed.
// You must hold idelock while manipulating queue.

static struct spinlock idelock;
static struct buf *idequeue;

static int havedisk1;
static void idestart(struct buf*);


// If B_DIRTY is set, write buf to disk, clear B_DIRTY, set B_VALID - increment wait_write_op.
// Else if B_VALID is not set, read buf from disk, set B_VALID - increment wait_read_op.
// increment wait_op anyway.
struct Ideinfo* get_ideinfo(){

  //cprintf("in get_ideinfo 1\n");
  //struct buf **pp;
  struct buf **bp;
  int i = 0;
  //int debug_c = 0;
  memset(Ideinfo.device_num, 0, BUFFSIZE);
  memset(Ideinfo.b_num, 0, BUFFSIZE);
  Ideinfo.wait_op = 0;
  Ideinfo.wait_read_op = 0;
  Ideinfo.wait_write_op = 0;
  
  acquire(&idelock);


  //cprintf("someeeee %d\n",((int) (&idequeue->qnext)));
  if(!(idequeue)){
    device_num_size = 0; 
    release(&idelock);
    return &Ideinfo;
  }
  for(bp=&idequeue->qnext; *bp /*&& (*bp)->qnext*/; bp = &(*bp)->qnext){  // cat proc/ideinfo
  //cprintf("bp %d\n",((int) (*bp)));
    //cprintf("in get_ideinfo loop  1 %d\n",debug_c++);
    Ideinfo.device_num[i] = (*bp)->dev;
    Ideinfo.b_num[i] = (*bp)->blockno;
    
    i++;
    Ideinfo.wait_op++;
    if((*bp)->flags & B_DIRTY)
      Ideinfo.wait_write_op++;
    else if(!((*bp)->flags & B_VALID))
      Ideinfo.wait_read_op++;
    //cprintf("in get_ideinfo loop  2 %d\n",debug_c);

  }



  //cprintf("in get_ideinfo--------initialized device_num_size %d\n",device_num_size);
  device_num_size = i;  
  release(&idelock);
  return &Ideinfo;
}

// Wait for IDE disk to become ready.
static int
idewait(int checkerr)
{
  int r;

  while(((r = inb(0x1f7)) & (IDE_BSY|IDE_DRDY)) != IDE_DRDY)
    ;
  if(checkerr && (r & (IDE_DF|IDE_ERR)) != 0)
    return -1;
  return 0;
}

void
ideinit(void)
{
  int i;

  initlock(&idelock, "ide");
  ioapicenable(IRQ_IDE, ncpu - 1);
  idewait(0);

  // Check if disk 1 is present
  outb(0x1f6, 0xe0 | (1<<4));
  for(i=0; i<1000; i++){
    if(inb(0x1f7) != 0){
      havedisk1 = 1;
      break;
    }
  }

  // Switch back to disk 0.
  outb(0x1f6, 0xe0 | (0<<4));
}

// Start the request for b.  Caller must hold idelock.
static void
idestart(struct buf *b)
{
  if(b == 0)
    panic("idestart");
  if(b->blockno >= FSSIZE)
    panic("incorrect blockno");
  int sector_per_block =  BSIZE/SECTOR_SIZE;
  int sector = b->blockno * sector_per_block;
  int read_cmd = (sector_per_block == 1) ? IDE_CMD_READ :  IDE_CMD_RDMUL;
  int write_cmd = (sector_per_block == 1) ? IDE_CMD_WRITE : IDE_CMD_WRMUL;

  if (sector_per_block > 7) panic("idestart");

  idewait(0);
  outb(0x3f6, 0);  // generate interrupt
  outb(0x1f2, sector_per_block);  // number of sectors
  outb(0x1f3, sector & 0xff);
  outb(0x1f4, (sector >> 8) & 0xff);
  outb(0x1f5, (sector >> 16) & 0xff);
  outb(0x1f6, 0xe0 | ((b->dev&1)<<4) | ((sector>>24)&0x0f));
  if(b->flags & B_DIRTY){
    outb(0x1f7, write_cmd);
    outsl(0x1f0, b->data, BSIZE/4);
  } else {
    outb(0x1f7, read_cmd);
  }
}

// Interrupt handler.
void
ideintr(void)
{
  struct buf *b;

  // First queued buffer is the active request.
  acquire(&idelock);

  if((b = idequeue) == 0){
    release(&idelock);
    return;
  }
  idequeue = b->qnext;

  // Read data if needed.
  if(!(b->flags & B_DIRTY) && idewait(1) >= 0)
    insl(0x1f0, b->data, BSIZE/4);

  // Wake process waiting for this buf.
  b->flags |= B_VALID;
  b->flags &= ~B_DIRTY;
  wakeup(b);

  // Start disk on next buf in queue.
  if(idequeue != 0)
    idestart(idequeue);

  release(&idelock);
}

